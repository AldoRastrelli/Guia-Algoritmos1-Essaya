def sumar(a,b):
	return a + b

def restar(a,b):
	return a - b

def dividir(a,b):
	return a / b

def multiplicar(a,b):
	return a * b

a = float(input("A: "))
b = float(input("B: "))

print("SUMA: ",sumar(a,b))
print("RESTA: ",restar(a,b))
print("DIVISIÓN de A/B: ",dividir(a,b))
print("MULTIPLICACIÓN: ",multiplicar(a,b))
